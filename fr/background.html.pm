◊chapter{À propos de Sozi}

◊section{Qui développe Sozi ?}

Je m'appelle Guillaume Savaton.
Je travaille comme ingénieur et enseignant-chercheur dans les domaines
de l'électronique numérique, des systèmes embarqués et du traitement numérique
du signal.

Le développement d'un outil de ◊em{bureautique} en utilisant les technologies du
web semble en décalage complet avec mes activités professionnelles.

Je développe Sozi sur mon temps libre.

Sozi n'est pas un ◊em{produit} commercial.
Il n'est développé ni par une entreprise, ni par une équipe de développeurs.
Il s'agit d'un projet personnel que je développe sur mon temps libre.

Pour cette raison, même si je fais de mon mieux pour aider les utilisateurs en
difficulté, le temps que je peux consacrer à Sozi est limité.

◊section{Pourquoi avoir développé Sozi ?}

Au cours de ma carrière, j'ai réalisé de nombreuses présentations dans le cadre
de mes activités d'enseignement et de recherche : cours, conférences scientifiques,
réunions diverses.

Comme tout un chacun, j'ai débuté dans les années 1990 en utilisant le logiciel
PowerPoint de Microsoft.
Rapidement, la découverte des logiciels libres et le passage sous GNU/Linux
m'ont amené à chercher des alternatives :
ce fut d'abord Impress, appartenant à la suite OpenOffice.org, puis
LibreOffice, et Beamer, une classe de documents pour LaTeX.

En 2009, ◊link["https://prezi.com/"]{Prezi} m'a été recommandé par un collègue
qui comptait l'utiliser pour sa soutenance de thèse.
L'idée m'a paru séduisante, mais les conditions d'utilisation ne me convenaient pas :
en effet, à l'époque, Prezi proposait une plate-forme fermée dans laquelle
l'édition, le stockage, la publication, et le visionnage des présentations
devait obligatoirement passer par leurs services en ligne.

La première version de Sozi a été écrite en un week-end, juste pour relever le
défi de développer un outil de présentation sur le même principe que Prezi,
mais en utilisant uniquement des formats ouverts et des logiciels libres.


◊section{Pourquoi ce nom "Sozi" ?}

Le nom "Sozi" est une déformation du mot français "Sosie", qui désigne une
personne ayant une forte ressemblance avec une autre personne, au point qu'on
peut les confondre.
Le choix de ce nom semble suggérer que Sozi est une imitation fidèle de Prezi,
mais ce n'est pas le cas.

Certes, Sozi emprunte l'idée de base de Prezi, où la présentation s'organise
comme l'exploration d'un plan, plutôt que comme une succession de diapos.
Mais en pratique, n'ayant moi-même jamais utilisé Prezi, j'ai développé Sozi
selon mes propres idées sans chercher à en reproduire toutes les fonctionnalités.

En choisissant ce nom, j'ai également négligé le sens qu'il pouvait prendre
dans d'autres langues.
En Allemand, "Sozi" est un terme familier, voire désobligeant, qui désigne un
partisan du socialisme ou de la social-démocratie.
Je l'ai compris par hasard en découvrant que le hashtag "#Sozi" sur Twitter
donnait de très nombreux résultats en allemand, visiblement sans rapport
avec mon logiciel.
Pour les éventuels lecteurs germanophones de ce livre, je tiens donc à préciser
que le choix du nom "Sozi" n'a pas de connotation politique.


